class Operations {

    public static void main(String[] args) {
        System.out.println("add 2+3 : " + add(2,3));
        System.out.println("subtract 3-2 : " + subtract(3,2));
    }

    public static int add(int num1,int num2){
        return num1+num2;
    }
    public static int subtract(int num1,int num2){
        return num1-num2;
    }
}
